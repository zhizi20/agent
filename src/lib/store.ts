// In-memory store
let feedbacks: Feedback[] = [...feedbackData];

export function getAllFeedbacks(): Feedback[] {
  return [...feedbacks];
}

export function getFeedbackById(id: string): Feedback | undefined {
  return feedbacks.find((f) => f.id === id);
}

export function getFeedbacksByCategory(category: FeedbackCategory): Feedback[] {
  return feedbacks.filter((f) => f.category === category);
}

export function getFeedbacksByFactory(factory: string): Feedback[] {
  return feedbacks.filter((f) => f.factory === factory);
}

export function createFeedback(data: {
  category: FeedbackCategory;
  title: string;
  description: string;
  department: string;
}): Feedback {
  const newFeedback: Feedback = {
    id: String(Date.now()),
    name: '匿名',
    department: data.department,
    category: data.category,
    title: data.title,
    description: data.description,
    handler: '',
    result: '',
    score: '',
    scoreContent: '',
    factory: '其他部门',
    urgency: 'normal',
    responsibleDept: '行政部',
    handleStatus: 'unresolved',
    createdAt: new Date().toISOString(),
  };
  feedbacks = [newFeedback, ...feedbacks];
  return newFeedback;
}

export function getFeedbackStats() {
  const byCategory: Record<string, number> = {};
  const byFactory: Record<string, number> = {};
  const byUrgency: Record<string, number> = { urgent: 0, high: 0, normal: 0 };
  const byResponsibleDept: Record<string, number> = {};
  const byHandleStatus: Record<string, number> = { resolved: 0, unresolved: 0 };
  let handledCount = 0;
  let totalScore = 0;
  let scoreCount = 0;

  for (const f of feedbacks) {
    byCategory[f.category] = (byCategory[f.category] || 0) + 1;
    byFactory[f.factory] = (byFactory[f.factory] || 0) + 1;
    byUrgency[f.urgency] = (byUrgency[f.urgency] || 0) + 1;
    byResponsibleDept[f.responsibleDept] = (byResponsibleDept[f.responsibleDept] || 0) + 1;
    byHandleStatus[f.handleStatus] = (byHandleStatus[f.handleStatus] || 0) + 1;
    if (f.handler) handledCount++;
    if (f.score) {
      const s = parseInt(f.score);
      if (!isNaN(s)) {
        totalScore += s;
        scoreCount++;
      }
    }
  }

  return {
    total: feedbacks.length,
    byCategory,
    byFactory,
    byUrgency,
    byResponsibleDept,
    byHandleStatus,
    handledCount,
    handleRate: feedbacks.length > 0 ? Math.round((handledCount / feedbacks.length) * 100) : 0,
    avgScore: scoreCount > 0 ? Math.round((totalScore / scoreCount) * 10) / 10 : 0,
    scoreCount,
  };
}

export function getTopIssues(): { category: string; count: number; percentage: number; samples: string[] }[] {
  const catMap: Record<string, { count: number; samples: string[] }> = {};
  
  for (const f of feedbacks) {
    if (!catMap[f.category]) {
      catMap[f.category] = { count: 0, samples: [] };
    }
    catMap[f.category].count++;
    if (catMap[f.category].samples.length < 3 && f.description) {
      catMap[f.category].samples.push(f.description.length > 80 ? f.description.slice(0, 80) + '...' : f.description);
    }
  }

  const total = feedbacks.length;
  return Object.entries(catMap)
    .map(([cat, data]) => ({
      category: cat,
      count: data.count,
      percentage: Math.round((data.count / total) * 1000) / 10,
      samples: data.samples,
    }))
    .sort((a, b) => b.count - a.count);
}

// Get feedbacks grouped by urgency
export function getFeedbacksByUrgency() {
  const urgent = feedbacks.filter(f => f.urgency === 'urgent');
  const high = feedbacks.filter(f => f.urgency === 'high');
  const normal = feedbacks.filter(f => f.urgency === 'normal');
  return { urgent, high, normal };
}
